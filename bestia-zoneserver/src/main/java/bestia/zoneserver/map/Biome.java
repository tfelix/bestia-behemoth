package bestia.zoneserver.map;

/**
 * Contains the type of biomes which are used and supported by the bestia map
 * generation system.
 * 
 * @author Thomas Felix
 *
 */
public enum Biome {
	WATER,
	DESERT,
	BEACH,
	FORREST,
	TROPICAL_FORREST,
	SWAMP,
	/**
	 * Warmer in summer (thaw). Snowfall, wetter.
	 */
	TAIGA,
	/**
	 * Permafrost, dry.
	 */
	TUNDRA,
	ALPINE,
	TEMPERATE_GRASSLAND,
	WETLANDS,
	SAVANNAH
}
