package net.bestia.palantir.map;

public class MapPosition {
	private long mapX;
	private long mapY;
	private long mapWidth;
	private long mapHeight;

	public MapPosition() {
	}

	public long getMapX() {
		return mapX;
	}

	public void setMapX(long mapX) {
		this.mapX = mapX;
	}

	public long getMapY() {
		return mapY;
	}

	public void setMapY(long mapY) {
		this.mapY = mapY;
	}

	public long getMapWidth() {
		return mapWidth;
	}

	public void setMapWidth(long mapWidth) {
		this.mapWidth = mapWidth;
	}

	public long getMapHeight() {
		return mapHeight;
	}

	public void setMapHeight(long mapHeight) {
		this.mapHeight = mapHeight;
	}
}