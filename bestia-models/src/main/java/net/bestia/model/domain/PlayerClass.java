package net.bestia.model.domain;

/**
 * Representing the different classes of player bestias. This changed the way
 * how some skill trees are expressed. Will also alter the status values
 * slightly.
 * 
 * @author Thomas Felix
 *
 */
public enum PlayerClass {

	/**
	 * Masters the connection to the bestias.
	 */
	SOUL_BINDER,

	/**
	 * Expells in research and discovery. Also is able to forge artisan
	 * masterpieces.
	 */
	SCHOLAR,

	/**
	 * Improves his own stamina and battle skills to be at the frontline in
	 * battles.
	 */
	KNIGHT

}
